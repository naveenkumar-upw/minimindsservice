        }
        .success {
            color: green;
            margin-bottom: 15px;
        }
        .auth-links {
            margin-top: 15px;
            text-align: center;
        }
    </style>
</head>
<body>
    <div class="auth-container">
        <h2>Register</h2>
        <?php if ($error): ?>
            <div class="error"><?php echo htmlspecialchars($error); ?></div>
        <?php endif; ?>
        <?php if ($success): ?>
            <div class="success"><?php echo htmlspecialchars($success); ?></div>
        <?php endif; ?>
        
        <form method="POST" action="">
